function yayup {
	yay -Syu --noconfirm
}

alias yayu=yayup
alias yau=yayup
alias yaup=yayup

function yayi {
	yay -S "$@" --noconfirm
}

alias yayin=yayi
alias yain=yayi
alias yai=yayi

function yayr {
	yay -Rsc "$@" --noconfirm
}

alias yayrm=yayr
alias yarm=yayr
alias yar=yayr

function yays {
	yay -Ss "$@"
}

alias yas=yays
